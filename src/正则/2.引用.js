/* ----------------------------------------- 普通捕获组引用 ----------------------------------------- */
/* 
- \n表示第n个捕获组 从1开始
*/
;(function () {
  console.log(/([a-zA-Z])\1/.test('abcc')) // true
  console.log(/([a-zA-Z])\1/.test('abc')) // false
})

/* ----------------------------------------- 具名捕获组引用 ----------------------------------------- */
/* 
- \k + 尖括号 + 捕获组名
- 索引引用依然有效
*/
;(function () {
  console.log(/(?<name>[a-zA-Z])\k<name>/.test('abcc')) // true
})